    private void tableDatas() {
        while (dm.getRowCount() > 0) {
            dm.removeRow(0);
        }

        try {
            df = new DatabaseFunctions();
            ArrayList<AddCustomer> l = df.findAll();
            l.forEach((list) -> {
                dm.addRow(new Object[]{
                    list.getCustid() + " -> "
                    + list.getN() + ", "
                    + list.getA1() + ", "
                    + list.getA2() + ", "
                    + list.getA3() + ", "
                    + list.getT() + ", "
                    + list.getSc() + ", "
                    + list.getPho()
                });
            });
        } catch (SQLException | ClassNotFoundException ex) {
            // don't do anything
        }
    }

