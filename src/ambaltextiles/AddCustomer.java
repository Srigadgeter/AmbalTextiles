    private void addAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAccountActionPerformed
        df = new DatabaseFunctions();
        try {
            if (!nameOfCompany.getText().equals("") && !addressLine1.getText().equals("") && !addressLine2.getText().equals("")
                    && !gstinNumber.getText().equals("") && !stateCode.getText().equals("") && !phoneNumber.getText().equals("") && phoneNumber.getText().length() < 12 && phoneNumber.getText().length() > 5) {
                df.insertionAccount(nameOfCompany.getText(), addressLine1.getText(), addressLine2.getText(),
                        addressLine3.getText(), gstinNumber.getText(), stateCode.getText(), phoneNumber.getText());
                tableDatas();
            } else if (!nameOfCompany.getText().equals("") && !addressLine1.getText().equals("") && !addressLine2.getText().equals("")
                    && !addressLine3.getText().equals("") && !gstinNumber.getText().equals("") && !stateCode.getText().equals("") && !phoneNumber.getText().equals("")
                     && (phoneNumber.getText().length() >= 12 || phoneNumber.getText().length() <= 5)) {
                JOptionPane.showMessageDialog(this, "You must enter valid phone number", "Number Error", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "You must enter all fields \n(optional field: Address Line3)", "Empty Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException | ClassNotFoundException ex) {
            // don't do anything
        }
    }//GEN-LAST:event_addAccountActionPerformed

    private void updateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAccountActionPerformed
        // TODO add your handling code here:
        df = new DatabaseFunctions();
        try {
            if (nameOfCompany.getText().equals("") && addressLine1.getText().equals("") && addressLine2.getText().equals("")
                    && addressLine3.getText().equals("") && gstinNumber.getText().equals("") && stateCode.getText().equals("") && phoneNumber.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "You must enter anyone of the fields to update", "Empty Error", JOptionPane.ERROR_MESSAGE);
            } else if (!phoneNumber.getText().equals("") && (phoneNumber.getText().length() >= 12 || phoneNumber.getText().length() <= 5)) {
                JOptionPane.showMessageDialog(this, "You must enter valid phone number", "Number Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if (!customerId.getText().equals("")) {
                    df.updateAccount(Integer.parseInt(customerId.getText()), nameOfCompany.getText(), addressLine1.getText(), addressLine2.getText(),
                            addressLine3.getText(), gstinNumber.getText(), stateCode.getText(), phoneNumber.getText());
                    clearAll();
                    customerId.setText("");
                    tableDatas();
                } else {
                    JOptionPane.showMessageDialog(this, "You must enter Customer Id field", "Empty Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException | ClassNotFoundException ex) {
            // don't do anything
        }
    }//GEN-LAST:event_updateAccountActionPerformed

